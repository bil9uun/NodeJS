const nemeh = (a, b) => {
  return a + b;
};
const hasah = (a, b) => {
  return a - b;
};
const urjih = (a, b) => {
  return a * b;
};
const huvaah = (a, b) => {
  return a / b;
};
const zereg2 = (a) => {
  return a ** 2;
};
const yzguur2 = (a) => {
  return Math.sqrt(a);
};
module.exports = { nemeh, hasah, urjih, huvaah, zereg2, yzguur2 };
